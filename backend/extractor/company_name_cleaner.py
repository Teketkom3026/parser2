"""Очистка названия компании от мусорных префиксов/суффиксов."""
import re

# Слова/фразы, которые НЕ являются названием компании
_JUNK_EXACT = {
    "команда", "наша команда", "наши специалисты", "наши сотрудники",
    "контакты", "контактная информация", "руководство", "о компании",
    "о нас", "сотрудники", "коллектив", "специалисты", "эксперты",
    "персонал", "состав", "структура", "менеджмент", "management",
    "our team", "team", "about us", "staff", "leadership", "contacts",
    "about", "people", "meet our team", "meet the team",
}

# Мусорные префиксы в title страницы
_JUNK_PREFIXES = [
    r"^команда\s+",
    r"^наша\s+команда\s+",
    r"^познакомьтесь\s+с\s+нашей\s+командой\s*",
    r"^контакты\s+",
    r"^руководство\s+компании\s+",
    r"^руководство\s+",
    r"^сотрудники\s+",
    r"^специалисты\s+",
    r"^коллектив\s+",
    r"^о\s+компании\s*[|:—\-–]?\s*",
    r"^о\s+нас\s*[|:—\-–]?\s*",
]

# Мусорные суффиксы
_JUNK_SUFFIXES = [
    r"\s*[|:—\-–]\s*руководство$",
    r"\s*[|:—\-–]\s*команда$",
    r"\s*[|:—\-–]\s*контакты$",
    r"\s*[|:—\-–]\s*сотрудники$",
    r"\s*[|:—\-–]\s*наша команда$",
    r"\s*[|:—\-–]\s*специалисты$",
    r"\s*:\s*опыт\s+и\s+профессионализм$",
    r"\s*\|\s*[a-z0-9]+\.(ru|com|net|org)$",
]

# Признаки, что это не название компании, а URL/домен
_DOMAIN_RE = re.compile(r'https?://|www\.|\.ru|\.com|\.net|\.org', re.I)


def clean_company_name(raw: str) -> str:
    """Очистить и нормализовать название компании."""
    if not raw:
        return ""

    name = raw.strip()

    # Если это URL — возвращаем пустую строку (лучше ничего)
    if _DOMAIN_RE.search(name):
        return ""

    # Точное совпадение с мусором
    if name.lower() in _JUNK_EXACT:
        return ""

    # Убираем мусорные префиксы
    for pat in _JUNK_PREFIXES:
        name = re.sub(pat, "", name, flags=re.IGNORECASE).strip()

    # Убираем мусорные суффиксы
    for pat in _JUNK_SUFFIXES:
        name = re.sub(pat, "", name, flags=re.IGNORECASE).strip()

    # Убираем лишние символы в начале/конце
    name = name.strip(" \t\n\r|:—-–")

    # После очистки осталась мусорная строка?
    if name.lower() in _JUNK_EXACT:
        return ""

    # Слишком длинно для названия (биография, описание)
    if len(name) > 120:
        return ""

    return name

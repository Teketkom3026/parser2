def classify_role_by_industry(position: str) -> str:
    """Классифицировать роль по отрасли деятельности."""
    if not position:
        return "Другое"

    pos_lower = position.lower()

    # Ищем совпадение в порядке приоритета
    for industry_name, keywords in _INDUSTRY_MAP:
        for kw in keywords:
            if kw in pos_lower:
                return industry_name

    return "Другое"

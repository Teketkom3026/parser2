def _capitalize_words(text: str) -> str:
    """Первое слово с заглавной, аббревиатуры — прописными."""
    if not text:
        return text
    words = text.split()
    result = []
    for i, word in enumerate(words):
        word_lower = word.lower().strip(".,;:")
        if word_lower in _ABBREVS:
            result.append(word.upper().strip(".,;:") + word[len(word.rstrip(".,;:")):])
        elif i == 0:
            result.append(word[0].upper() + word[1:] if word else word)
        else:
            result.append(word)
    return " ".join(result)


def normalize_position_text(raw: str) -> str:
    """Нормализовать должность: именительный падеж, заглавная буква, аббревиатуры."""
    if not raw:
        return ""

    cleaned = clean_position_raw(raw)
    if not cleaned:
        return ""

    pos_lower = cleaned.lower().strip()

    # Точный маппинг
    if pos_lower in _NORM_MAP:
        return _NORM_MAP[pos_lower]

    # Поиск подстрок в маппинге (для длинных фраз)
    for key, val in sorted(_NORM_MAP.items(), key=lambda x: -len(x[0])):
        if key in pos_lower:
            # Заменяем фразу и возвращаем
            return _capitalize_words(
                re.sub(re.escape(key), val, cleaned, flags=re.IGNORECASE)
            )

    # Нормализация падежей слово за словом
    normalized = cleaned
    for pattern, replacement in _SUFFIX_MAP:
        normalized = pattern.sub(replacement, normalized)

    return _capitalize_words(normalized)


